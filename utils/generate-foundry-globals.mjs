#!/usr/bin/env node
// Regenerate eslint.foundry-globals.mjs from the Foundry source under REFERENCE/.
//
// Scans REFERENCE/{12,13,14}/'s client bundle entry points for global identifiers,
// unions them across all available versions, and writes the merged list to
// eslint.foundry-globals.mjs.
//
// Usage: npm run lint:globals:refresh
//
// Requires REFERENCE/ symlinks set up via `npm run foundry:reference:link` for
// any version you want included. Missing versions are skipped with a warning.

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import prettier from "prettier";

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

// Per-version: relative paths from REFERENCE/<ver>/ to the bundled client entry points.
// v12 bundled under resources/app/; v13+ flattened.
const VERSION_SOURCES = {
  12: ["resources/app/client-esm/client.mjs", "resources/app/public/scripts/foundry-esm.js"],
  13: ["client/client.mjs", "public/scripts/foundry.mjs"],
  14: ["client/client.mjs", "public/scripts/foundry.mjs"],
};

// Globals that aren't attached via the patterns this script detects, or come from
// third-party libraries Foundry bundles. Add/remove as the system's usage evolves.
const EXTRAS = [
  "foundry", // The main foundry.* namespace
  "$", // jQuery shorthand
  "jQuery",
  "PIXI",
  "Handlebars",
  "io", // socket.io client

  // Foundry globals attached at runtime via paths the static scanner misses
  // (e.g. window.canvas = new Canvas() inside an init flow rather than at top-level)
  "canvas",
  "Actors", // collection class — global, separate from game.actors
  "Items",
  "FormDataExtended",
  // Deprecated V1 Application classes — still globals on v12/v13/v14 until v16
  "ActorSheet",
  "ItemSheet",
  "ModuleManagement",
  // Deprecated handlebars helpers — still globals on v12/v13, foundry.applications.handlebars.* on v14
  "renderTemplate",
  "loadTemplates",

  // System-specific module integrations the codebase references
  "PoolTerm",
  "Sequencer",
  "Sequence",
  "dragRuler",
  "AutoAnimations",
];

/**
 * Extract identifiers that appear to be attached to globalThis from a JS source file.
 * Captures two patterns:
 *  - Object.assign(globalThis, { Key1: ..., Key2 })
 *  - globalThis.NAME = ...
 */
function extractGlobalsFromFile(filePath) {
  if (!existsSync(filePath)) return new Set();
  const src = readFileSync(filePath, "utf8");
  const found = new Set();

  // Object.assign(globalThis, { ... }) blocks
  const blockRegex = /Object\.assign\(\s*globalThis\s*,\s*\{([\s\S]*?)^\}\s*\)/gm;
  let blockMatch;
  while ((blockMatch = blockRegex.exec(src)) !== null) {
    const body = blockMatch[1];
    // Keys on their own line: optional indent, identifier, then `,` (shorthand) or `:` (full)
    const keyRegex = /^\s+([A-Za-z_$][A-Za-z0-9_$]*)\s*[,:]/gm;
    let keyMatch;
    while ((keyMatch = keyRegex.exec(body)) !== null) {
      found.add(keyMatch[1]);
    }
  }

  // globalThis.NAME = ... direct assignments
  const directRegex = /globalThis\.([A-Za-z_$][A-Za-z0-9_$]*)\s*=/g;
  let directMatch;
  while ((directMatch = directRegex.exec(src)) !== null) {
    found.add(directMatch[1]);
  }

  return found;
}

const allGlobals = new Set(EXTRAS);
const versionSummary = [];

for (const [version, relativePaths] of Object.entries(VERSION_SOURCES)) {
  const versionRoot = path.join(REPO_ROOT, "REFERENCE", String(version));
  if (!existsSync(versionRoot)) {
    versionSummary.push(`v${version}: skipped (REFERENCE/${version}/ not linked)`);
    continue;
  }
  const versionGlobals = new Set();
  for (const rel of relativePaths) {
    const fullPath = path.join(versionRoot, rel);
    for (const g of extractGlobalsFromFile(fullPath)) versionGlobals.add(g);
  }
  versionGlobals.forEach((g) => allGlobals.add(g));
  versionSummary.push(`v${version}: ${versionGlobals.size} globals`);
}

if (allGlobals.size === EXTRAS.length) {
  console.error("No REFERENCE/{12,13,14}/ versions were available; nothing to extract.");
  console.error("Run `npm run foundry:reference:link` first.");
  process.exit(1);
}

const sorted = [...allGlobals].sort((a, b) => a.localeCompare(b));
const globalsObject = Object.fromEntries(sorted.map((k) => [k, "readonly"]));

const header = `// Foundry VTT global identifiers used by ESLint.
//
// AUTO-GENERATED by utils/generate-foundry-globals.mjs from REFERENCE/{12,13,14}/.
// Refresh after a Foundry update: npm run lint:globals:refresh
// Source coverage: ${versionSummary.join(" | ")}

export default `;

const output = path.join(REPO_ROOT, "eslint.foundry-globals.mjs");
const raw = header + JSON.stringify(globalsObject, null, 2) + ";\n";
const prettierConfig = await prettier.resolveConfig(output);
const formatted = await prettier.format(raw, { ...prettierConfig, parser: "babel" });
writeFileSync(output, formatted);

console.log(`Wrote ${output}`);
console.log(`  ${sorted.length} unique globals`);
console.log(`  ${versionSummary.join(" | ")}`);
